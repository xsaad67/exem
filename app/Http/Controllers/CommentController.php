    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'comment' => 'required|min:5',
            'post' => 'required',
        ],[
            'comment.required' => 'Comment must be greater than 5 characters',
            'comment.min' => 'Comment body must be greater than 5 characters',
        ]);      
        $comment = new Comment();
        $comment->body = $request->comment;
        $comment->user_id = auth()->id();
        $comment->post_id = decrypt($request->post);
        $comment->save();
        $strHtml = view('comments.show')->with('comment',$comment)->render();
        return response()->json(['success'=>true,'html'=>$strHtml]);
    }

